var numeroTres = prompt("Ingrese el primer numero por favor:");
var numeroCuatro = prompt("Ingrese el segundo numero por favor:");

if(numeroTres == numeroCuatro) {
    alert("Son iguales.");
}else if(numeroTres > numeroCuatro) {
    alert("El primer numero es mayor que el segundo.")
}else if(numeroCuatro > numeroTres){
    alert("El segundo numero es mayor que el primero")
}